void morse_(const char *msg, int *idat, int *n, size_t msg_hidden_len)
{
    memset(idat, 0, 250 * sizeof(int));
    int cn = 6;  /* next write index (0-based), matches Fortran n=6 */

    for (size_t k = 0; k < msg_hidden_len; k++) {
        int jj = (unsigned char)msg[k];
        if (jj >= 97 && jj <= 122) jj -= 32;  /* lower → upper */

        int j;
        if      (jj >= 48 && jj <= 57) j = jj - 48;   /* '0'-'9' → 0-9  */
        else if (jj >= 65 && jj <= 90) j = jj - 55;   /* 'A'-'Z' → 10-35 */
        else if (jj == 47)             j = 36;          /* '/'             */
        else if (jj == 32)             j = 37;          /* ' '             */
        else                           continue;         /* ignore unknown  */

        int nmax = ic[j][20];
        if (cn + nmax + 4 > 250) break;

        for (int i = 0; i < nmax; i++)
            idat[cn++] = ic[j][i];
        idat[cn++] = 0;   /* 2-dit character separator (2 of 3) */
        idat[cn++] = 0;
    }

    /* Trailing word space: 4 zeros (pattern already ends with 0, total = 7) */
    for (int i = 0; i < 4; i++)
        idat[cn++] = 0;

    *n = cn;
}
