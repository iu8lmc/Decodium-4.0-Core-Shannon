void getmet4_(int *mettab, int *ndelta)
{
    static const float bias  = 0.5f;
    static const float scale = 50.0f;

    *ndelta = (int)roundf(3.4f * scale);   /* = 170 */

    for (int i = 0; i <= 255; i++) {
        float xx = xx0[i];
        if (i >= 160)
            xx = xx0[160] - (float)(i - 160) * 6.822f / 65.3f;

        /* mettab(i-128, 0) → flat index i */
        mettab[i] = (int)roundf(scale * (xx - bias));

        /* mettab(128-i, 1) → flat index 512-i   (skip i=0: would be 512, out of bounds) */
        if (i >= 1)
            mettab[512 - i] = mettab[i];
    }

    /* mettab(-128, 1) = mettab(-127, 1) → mettab[256] = mettab[257] */
    mettab[256] = mettab[257];
}
